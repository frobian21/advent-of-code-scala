import scala.util.matching.Regex

object Day2 {

  val movePattern: Regex = "([A-Z]+) ([A-Z]+)".r

  val matches = (input: String) => movePattern.findAllMatchIn(input).toList  

  def parseMoves(input: String): List[(Move, Move)] =
    matches(input).map(m => (Move(m.group(1)), Move(m.group(2))))

  def parseMovesAndResults(input: String): List[(Move, Result)] =
    matches(input).map(m => (Move(m.group(1)), Result(m.group(2))))

  def getRoundScores(rounds: List[(Move, Value)]): List[Int] =
    rounds.map{(m1, v) => v match {
        case r: Result => r.value + Move(m1, r).value
        case m2: Move => Result(m1, m2).value + m2.value
      }
    }

  def getTotalScore(rounds: List[(Move, Value)]): Int =
    getRoundScores(rounds).sum
}

trait Value:
    val value: Int

enum Result(val value: Int) extends Value:
  case Win extends Result(6)
  case Draw extends Result(3)
  case Loss extends Result(0)
object Result:
  def apply(move1: Move, move2: Move): Result = (move1, move2) match
    case (Move.Rock, Move.Paper) | (Move.Paper, Move.Scissors) | (Move.Scissors, Move.Rock) => 
      Win
    case (a, b) if a == b => Draw
    case _ => Loss

  def apply(string: String): Result = string match
    case "X" => Loss
    case "Y" => Draw
    case "Z" => Win
    case _ => throw new NoSuchElementException(string)

enum Move(val value: Int) extends Value:
  case Rock extends Move(1)
  case Paper extends Move(2)
  case Scissors extends Move(3)
object Move:
  def apply(string: String): Move = string match
    case "A" | "X" => Rock
    case "B" | "Y" => Paper
    case "C" | "Z" => Scissors
    case _ => throw new NoSuchElementException(string)
  def apply(move1: Move, result: Result): Move = result match
    case Result.Draw => move1
    case result => Move.values.filter(_ != move1).filter(move2 => Result(move1, move2) == result).head


